function soma(a,b){
    return a + b;
};


function subtracao(a,b){
    return a - b;
}


function divisao(a,b){
    return a / b;
}


function multiplicacao(a,b){
    return a * b;
}

console.log(soma(10,10)) 
console.log(subtracao(30,10))
console.log(divisao(10,2))
console.log(multiplicacao(5,2))
